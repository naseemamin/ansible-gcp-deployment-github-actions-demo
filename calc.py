"""
    Calculator app
"""


def add(num_1, num_2):
    """
    Function to add two numbers 
    :param num_2: first number
    :param num_2: second number
    :return: 
    """
    return num_1 + num_2


def subtract(num_1, num_2):
    """
    Function to subtract two numbers 
    :param num_2: first number
    :param num_2: second number
    :return: 
    """
    return num_1 - num_2
